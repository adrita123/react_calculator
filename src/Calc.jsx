
function addNumber(a,b){
    let sum = a+b;
    return sum;
}
function substractNumber(a,b){
    let sub = a-b;
    return sub;
}
function multiplyNumber(a,b){
    let mult = a*b;
    return mult;
}
function devideNumber(a,b){
    let dev = a/b;
    return dev;
}

export {addNumber,substractNumber,multiplyNumber,devideNumber};